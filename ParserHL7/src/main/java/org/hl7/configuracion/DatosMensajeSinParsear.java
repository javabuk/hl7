package org.hl7.configuracion;

public class DatosMensajeSinParsear {
	
	private String concepto;
	
	private String rutaParser;
	
	private String valor;

	public String getConcepto() {
		return concepto;
	}

	public void setConcepto(String concepto) {
		this.concepto = concepto;
	}

	public String getRutaParser() {
		return rutaParser;
	}

	public void setRutaParser(String rutaParser) {
		this.rutaParser = rutaParser;
	}

	public String getValor() {
		return valor;
	}

	public void setValor(String valor) {
		this.valor = valor;
	}
	
	

}
